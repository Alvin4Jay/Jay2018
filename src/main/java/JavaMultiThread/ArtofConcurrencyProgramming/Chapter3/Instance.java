package JavaMultiThread.ArtofConcurrencyProgramming.Chapter3;

/**
 * Model
 *
 * @author xuanjian.xuwj
 */
public class Instance {
	private int age;
	private String name;

	public Instance() {
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
